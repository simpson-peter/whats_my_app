import 'dart:math';
import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';
import 'package:whats_my_app/constants.dart';
import 'package:whats_my_app/util.dart';
import 'package:whats_my_app/widgets/next_button.dart';
import 'package:whats_my_app/widgets/share_dialog.dart';

class ResultPage extends StatefulWidget {
  //List of fonts to pull result name font from
  static List<TextStyle> resultFonts = [
    GoogleFonts.lobster(textStyle: kResultNameTextStyle),
    GoogleFonts.righteous(textStyle: kResultNameTextStyle),
    GoogleFonts.fredokaOne(textStyle: kResultNameTextStyle),
    GoogleFonts.patuaOne(textStyle: kResultNameTextStyle),
    GoogleFonts.baloo(textStyle: kResultNameTextStyle),
    GoogleFonts.londrinaOutline(textStyle: kResultNameTextStyle),
  ];

  static String id = "/result";
  final Function onButtonPress;
  ResultPage({@required this.onButtonPress});
  @override
  _ResultPageState createState() => _ResultPageState();
}

class _ResultPageState extends State<ResultPage> {
  //Tracks the intro phrase currently being displayed to the user
  String currentIntroPhrase = "";

  //Tracks the app name currently being displayed to the user
  String currentName = "";

  //Returns a text style from the result font list, tagging it with the substyle
  TextStyle getTextStyle() {
    return Util.getElementFromList(ResultPage.resultFonts);
  }

  //Returns the full app name to display
  String getAppName() {
    currentName = Util.getElementFromList(kFirstPhraseList) +
        Util.getElementFromList(kSecondPhraseList);
    return currentName;
  }

  String getIntroPhrase() {
    currentIntroPhrase = Util.getElementFromList(kIntroPhrases);
    return currentIntroPhrase;
  }

  //Handles behavior (loading twitter intent link) after user clicks on tweet button
  void onTweet() {
    String url = 'https://twitter.com/intent/tweet?text=';
    String message = currentIntroPhrase +
        ' ' +
        currentName +
        '. %0Generated by What\'s My App.';
    url = url + message;
    url = Util.convertToURLForm(url);
    Util.goToLink(url);
  }

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisAlignment: MainAxisAlignment.center,
      crossAxisAlignment: CrossAxisAlignment.center,
      children: [
        SizedBox(
          height: 55,
        ),
        Text(
          '"' + getIntroPhrase() + '"',
          style: kDisplayTextStyle.copyWith(
            fontWeight: FontWeight.normal,
            fontStyle: FontStyle.italic,
            fontSize: 25,
          ),
        ),
        SizedBox(
          height: 20,
        ),
        Container(
          height: 100,
          child: Text(
            getAppName(),
            style: getTextStyle(),
          ),
        ),
        SizedBox(
          height: 40,
        ),
        ShareDialog(
          onTweet: () {
            onTweet();
          },
        ),
        SizedBox(
          height: 10,
        ),
        NextButton(
          label: 'Give Me Another!',
          onPress: widget.onButtonPress,
        ),
      ],
    );
  }
}
